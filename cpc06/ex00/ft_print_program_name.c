#include <unistd.h>

int	main(int argc, char *argv[])
{
	char *c;

	if (argc < 1)
		return (1);
	c = argv[0];
	while (*c != 0)
	{
		write(1, c, 1);
		c++;
	}
	write(1, "\n", 1);
	return (0);
}
