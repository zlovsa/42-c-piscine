char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char *d;

	d = dest;
	while (*d != 0)
		d++;
	while (*src != 0 && nb != 0)
	{
		*d = *src;
		d++;
		src++;
		nb--;
	}
	*d = 0;
	return (dest);
}
